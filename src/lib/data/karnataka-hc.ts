// Department stats are aggregated from KHC_ACTIONS so they always match the
// underlying records — no risk of stale demo numbers.
export function buildDepartmentStats(): DepartmentStats[] {
  const colors: Record<string, string> = {
    'Urban Development': '#8E6B3E',
    'Environment': '#2D6A4F',
    'Transport': '#5B8DB8',
    'Energy': '#0D7377',
    'Revenue': '#7C3AED',
    'Home Affairs': '#185FA5',
    'Higher Education': '#7F77DD',
    'Finance': '#D4831A',
    'Labour': '#9A3B72',
    'Social Welfare': '#4A7C59',
    'Health': '#C1121F',
  };

  const buckets: Record<string, { total: number; completed: number; in_progress: number; overdue: number; pending: number }> = {};
  for (const a of KHC_ACTIONS) {
    const dept = a.department;
    const b = buckets[dept] ?? (buckets[dept] = { total: 0, completed: 0, in_progress: 0, overdue: 0, pending: 0 });
    b.total += 1;
    if (a.status === 'completed') b.completed += 1;
    else if (a.status === 'in_progress' || a.status === 'verified' || a.status === 'assigned') b.in_progress += 1;
    else if (a.status === 'overdue') b.overdue += 1;
    else b.pending += 1;
  }

  return Object.entries(buckets).map(([dept, b]) => {
    const pct = Math.round((b.completed / Math.max(1, b.total)) * 100);
    // Sparkline derived from the compliance pct so it's always plausible.
    const spark: number[] = [];
    for (let i = 0; i < 7; i++) {
      spark.push(Math.max(0, Math.round(pct * (0.55 + i * 0.075))));
    }
    return {
      department: dept,
      color_hex: colors[dept] ?? '#6B7280',
      total: b.total,
      completed: b.completed,
      in_progress: b.in_progress,
      overdue: b.overdue,
      pending: b.pending,
      compliance_pct: pct,
      sparkline: spark,
    };
  });
}

export const KHC_DEPARTMENT_STATS = buildDepartmentStats();
